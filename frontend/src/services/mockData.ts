// Helper function to generate reconciliation act
export const generateReconciliationAct = (clientId: number, startDate: string, endDate: string): ReconciliationAct | null => {
  const client = mockClients.find(c => c.id === clientId)
  if (!client) return null

  // Get transactions for this client
  const clientTransactions = mockTransactions.filter(t => t.client === client.name)
  const clientDebts = mockDebts.filter(d => d.type === 'CLIENT' && d.counterparty === client.name)

  const reconciliationTransactions: ReconciliationTransaction[] = []

  // Add income transactions
  clientTransactions.forEach(t => {
    if (t.type === 'INCOME') {
      reconciliationTransactions.push({
        id: t.id,
        date: t.date,
        description: t.description,
        type: 'INCOME',
        amount: t.amount,
        documentNumber: `ПП-${t.id}`,
      })
    }
  })

  // Add debt transactions
  clientDebts.forEach(d => {
    reconciliationTransactions.push({
      id: d.id + 1000,
      date: d.createdAt,
      description: d.description,
      type: 'EXPENSE',
      amount: d.remainingAmount,
      documentNumber: `ЗАД-${d.id}`,
    })
  })

  const totalIncome = reconciliationTransactions
    .filter(t => t.type === 'INCOME')
    .reduce((sum, t) => sum + t.amount, 0)

  const totalExpense = reconciliationTransactions
    .filter(t => t.type === 'EXPENSE')
    .reduce((sum, t) => sum + t.amount, 0)

  return {
    clientId,
    clientName: client.name,
    period: 'Июнь 2024',
    startDate,
    endDate,
    transactions: reconciliationTransactions,
    totalIncome,
    totalExpense,
    balance: totalIncome - totalExpense,
    generatedAt: new Date().toISOString(),
  }
}

